def add(x, y):
    """加法"""
    x = int(x)
    y = int(y)
    return  x + y

def sub(x, y):
    """减法"""
    x = int(x)
    y = int(y)
    return x - y

def mul(x, y):
    """乘法"""
    x = int(x)
    y = int(y)
    return x * y

def div(x, y):
    """除法"""
    x = int(x)
    y = int(y)
    return x/y